package vn.edu.hcmut.bkareer.common;

/**
 *
 * @author Kiss
 */
public class FileMeta {
	private final String name;
	private final String url;

	public FileMeta(String name, String url) {
		this.name = name;
		this.url = url;
	}

	public String getName() {
		return name;
	}

	public String getUrl() {
		return url;
	}
}
